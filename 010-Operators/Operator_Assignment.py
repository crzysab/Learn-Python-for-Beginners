# x + y
a = 2
b = 3
c = a + b
print("a + b : ",c)

# x+=y -> x = x + y
d = 4
e = 6
d += e
print("d += e : ",d)

# x-=y -> x = x - y
f = 3
g = 2
f -= g
print("f -= g : ",f)

# x*=y -> x = x*y
h = 4
i = 5
h*=i
print("h *= i : ", h)

# x/=y -> x = x/y
j = 8
k = 2
j/=k
print("j /= k : ",j)

# x**=y -> x = x**y
l = 2
m = 3
l**=m
print("l **= m : ", l)
#Function 'all'
a = (1,2,3,4)
print("Result a : ", all(a))
b = (1,2,3,4,0)
print("Result b : ", all(b))

#Function 'any'
c = (1,2,0)
print("Result c : ", any(c))
d = (0,0,0)
print("Result d : ", any(d))

#Function 'len'
e = (1,2,3,4,5,6)
print("Number of element : ", len(e))

#Function 'sum'
f = (1,2,3,4,5)
print("Sum of all : ", sum(f))
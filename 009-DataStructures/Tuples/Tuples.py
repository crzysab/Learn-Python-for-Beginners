#Number Tuples
MyNumTup = (1,2,3,4,5)
tupType = type(MyNumTup)
print("MyNumTup : ", MyNumTup)
print("My Data Structure is : ", tupType)

#Str Tuples
MyStrTup = ('a','b','c')
tupType2 = type(MyStrTup)
print("MyStrTup : ", MyStrTup)
print("My Data Structure is : ", tupType2)

#Str + Num Tuples
MyNumStrTup = (1,'b',2,'c')
tupType3 = type(MyNumStrTup)
print("MyNumStrTup : ", MyNumStrTup)
print("My Data Structure is : ", tupType3)
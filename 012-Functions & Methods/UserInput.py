#Default DataType for input is 'str'
a = input("Write the first value  : ")
b = input("Write the second value : ")
c = a + b
print("First result : ",c)

#Set DataType to 'int'
x = int(input("Write the first value  : "))
y = int(input("Write the second value : "))
z = x + y
print("Second result : ",z)